#include "main.h"
/**
 * _memcpy - A function that copies memory area
 *
 * @dest: Destination of memory storage
 * @src: Source of memory storage
 * @n: number of bytes
 *
 * Return: A pointer to destination
 */
char *_memcpy(char *dest, char *src, unsigned int n)
{
	int a;
	int b;

	a = 0;
	n = n;

	for (; a < b; a++)
	{
		dest[a] = src[a];
		n--;
	}
	return (dest);
}
